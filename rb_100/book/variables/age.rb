# Write a program that asks for the user's age and then prints out how old
# the user will be in 10, 20, 30 and 40 years

print "Please enter your age: "
age = gets.chomp.to_i

puts "In 10 years you will be:"
puts age + 10
puts "In 20 years you will be:"
puts age + 20
puts "In 30 years you will be:"
puts age + 30
puts "In 40 years you will be:"
puts age + 40

