=begin
Tic-tac-toe is a 2-player game played on a board of 3x3 squares. Players take turns marking
the board inside a square. The first player to reach 3 marks in a row (including diagonals)
wins the game. If all 9 squares are full and neither player has 3 in a row, the game is a
tie.

Steps:
1. Display the blank board
2. Determine which player's turn it is.
3. Get input from that player to determine which square to mark
4. Display the updated board with the player's mark in the square
5. Determine if the game is over.
6. If the game is over and player has won, display the winner and ask to play again.
7. If the game is over because the board is full, display a tie and ask to play again.
8. If the game is not over, return to step 2.

Given Steps:
1. Display the initial empty 3x3 board.
2. Ask the user to mark a square.
3. Computer marks a square.
4. Display the updated board state.
5. If winner, display winner.
6. If board is full, display tie.
7. If neither winner nor board is full, go to #2
8. Play again?
9. If yes, go to #1
10. Good bye!


=end
